package day12ArrayList;

public class MethotCreation {
    public static int topla(int a,int b){

        return a+b;
    }
    public static void main(String[] args) {
        /*
        METOT OLUŞTURMA
        Access Modifier + return Type + isim (){}
        main metodun dışında, class'ın içinde oluşturulur
         */
        System.out.println(topla(4,6));
    }
}
